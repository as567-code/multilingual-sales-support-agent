"""Aggregate Stage-10 framework benchmark results into a ranked markdown
table. Picks up whatever ``evals/results/bench_*.json`` files exist and
prints them sorted by recall@k (desc) then MRR.

Missing frameworks show up as ``not run`` rows so the report makes clear
which were actually measured vs. merely pinned as optional dependencies.
"""
from __future__ import annotations

import argparse
import json
from pathlib import Path

RESULTS_DIR = Path("evals/results")
KNOWN = ["langchain_faiss", "llamaindex", "haystack", "txtai", "ragatouille"]


def _load(name: str) -> dict | None:
    p = RESULTS_DIR / f"bench_{name}.json"
    if not p.exists():
        return None
    return json.loads(p.read_text())


def _fmt_pct(x: float | None) -> str:
    return f"{100 * x:5.1f}%" if x is not None else "   —  "


def _fmt_num(x: float | None, unit: str = "") -> str:
    return f"{x:,.1f}{unit}" if x is not None else "—"


def build_table() -> str:
    rows = []
    for name in KNOWN:
        d = _load(name)
        if d is None:
            rows.append({"name": name, "status": "not run"})
        elif d.get("error"):
            rows.append({"name": name, "status": f"error: {d['error']}"})
        else:
            rows.append(
                {
                    "name": name,
                    "status": "ok",
                    "recall_at_k": d["recall_at_k"],
                    "mrr": d["mrr"],
                    "qps": d["qps"],
                    "index_ms": d["index_ms"],
                    "k": d["k"],
                    "per_lang": d.get("per_lang", {}),
                }
            )

    ok = [r for r in rows if r.get("status") == "ok"]
    ok.sort(key=lambda r: (r.get("recall_at_k", 0), r.get("mrr", 0)), reverse=True)
    other = [r for r in rows if r.get("status") != "ok"]
    ordered = ok + other

    lines = [
        "# Framework bake-off — Stage 10",
        "",
        "Retrieval-only comparison. All frameworks indexed the same 420-chunk",
        "FAQ corpus (EN + ES + HI) and were evaluated on the same 930-query",
        "gold set. Metric: recall@k and MRR over the full gold, plus wall-",
        "clock index build time and single-threaded QPS on CPU.",
        "",
        "| Rank | Framework | Status | Recall@k | MRR | QPS | Index time |",
        "|-----:|-----------|:-------|---------:|----:|----:|-----------:|",
    ]
    for i, r in enumerate(ordered, 1):
        if r.get("status") != "ok":
            lines.append(f"| — | {r['name']} | _{r['status']}_ | — | — | — | — |")
        else:
            lines.append(
                "| {i} | **{n}** | ok | {rk} @{k} | {mrr} | {qps} | {idx} |".format(
                    i=i,
                    n=r["name"],
                    rk=_fmt_pct(r["recall_at_k"]),
                    k=r["k"],
                    mrr=f"{r['mrr']:.3f}",
                    qps=_fmt_num(r["qps"], " q/s"),
                    idx=_fmt_num(r["index_ms"], " ms"),
                )
            )

    # Per-language breakdown for any framework that produced it.
    per_lang_rows = [r for r in ok if r.get("per_lang")]
    if per_lang_rows:
        lines += [
            "",
            "## Per-language recall@k",
            "",
            "| Framework | en | es | hi |",
            "|-----------|---:|---:|---:|",
        ]
        for r in per_lang_rows:
            pl = r["per_lang"]
            lines.append(
                "| {n} | {en} | {es} | {hi} |".format(
                    n=r["name"],
                    en=_fmt_pct(pl.get("en", {}).get("recall_at_k")),
                    es=_fmt_pct(pl.get("es", {}).get("recall_at_k")),
                    hi=_fmt_pct(pl.get("hi", {}).get("recall_at_k")),
                )
            )

    lines += [
        "",
        "---",
        "",
        "_Generated by `python -m evals.framework_benchmark.compare`. Run individual",
        "benches via `python -m evals.framework_benchmark.bench_<name>`._",
    ]
    return "\n".join(lines) + "\n"


def main() -> None:
    ap = argparse.ArgumentParser()
    ap.add_argument("--out", default="evals/results/framework_bakeoff.md")
    args = ap.parse_args()
    out = Path(args.out)
    out.parent.mkdir(parents=True, exist_ok=True)
    out.write_text(build_table())
    print(f"wrote {out}")


if __name__ == "__main__":
    main()
